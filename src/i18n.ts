export const LANGUAGE_STORAGE_KEY = 'careconnect.language';

export const SUPPORTED_LANGUAGES = [
  { id: 'en', labelKey: 'english' },
  { id: 'ms', labelKey: 'malay' },
  { id: 'zh', labelKey: 'chinese' },
  { id: 'ta', labelKey: 'tamil' },
] as const;

export type SupportedLanguage = (typeof SUPPORTED_LANGUAGES)[number]['id'];

export function getSavedLanguage(): SupportedLanguage {
  const savedLanguage = localStorage.getItem(LANGUAGE_STORAGE_KEY);

  return SUPPORTED_LANGUAGES.some((language) => language.id === savedLanguage)
    ? savedLanguage as SupportedLanguage
    : 'en';
}

